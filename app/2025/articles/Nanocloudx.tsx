        <p>重要なのは、全ての候補者には同じ個数の７つ道具が配布されているという点です。資金や組織力のある候補者が有利にならないように、公職選挙法で個数が厳格に定められています。個数制限があることで、特定の候補者が選挙カーを沢山走らせたり、同時に複数箇所で演説するなどの行為が未然に防がれているのです。</p>
