        <p>国会議事堂の向かい側には、議員会館という国会議員や秘書が執務で利用する建物が並んでいます。全ての議員が常にいるわけではないのですが、特に国会会期中は大半の議員がこの建物に在室しています。議員室の中は「議員部屋・応接部屋・執務部屋・給湯室」の４区画になっており、机と椅子と本棚が備え付けられています。</p>
